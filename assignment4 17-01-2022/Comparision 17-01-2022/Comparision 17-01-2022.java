public class Comparision{
	public static void main (String args[]){
		int firstNumber=5;
		int secondNumber=10;
		if(firstNumber == secondNumber){
			System.out.printf("%d = %d\n",firstNumber,secondNumber);
		}
		else if(firstNumber != secondNumber){
			System.out.printf("%d = %d\n",firstNumber,secondNumber);
		}
	} 
}
